bool is_edge_port(uint64_t dp_id, uint32_t port){
    if (dp_id > 16)
    {
        return false;
    }

    int i;
    for (i = 0; i < hosts_of_swith[dp_id -1]; ++i)
    {
        if( port == edge_port[dp_id - 1][i] )
            return true;
    }

    return false;
}
